package com.dream.cutepet.model;

public class DynamicDetailsData {

	int dynamic_details_portrait;
	String dynamic_details_neckname;
	String dynamic_details_comment_content;
	String dynamic_details_comment_time;

	public int getDynamic_details_portrait() {
		return dynamic_details_portrait;
	}

	public void setDynamic_details_portrait(int dynamic_details_portrait) {
		this.dynamic_details_portrait = dynamic_details_portrait;
	}

	public String getDynamic_details_neckname() {
		return dynamic_details_neckname;
	}

	public void setDynamic_details_neckname(String dynamic_details_neckname) {
		this.dynamic_details_neckname = dynamic_details_neckname;
	}

	public String getDynamic_details_comment_content() {
		return dynamic_details_comment_content;
	}

	public void setDynamic_details_comment_content(
			String dynamic_details_comment_content) {
		this.dynamic_details_comment_content = dynamic_details_comment_content;
	}

	public String getDynamic_details_comment_time() {
		return dynamic_details_comment_time;
	}

	public void setDynamic_details_comment_time(
			String dynamic_details_comment_time) {
		this.dynamic_details_comment_time = dynamic_details_comment_time;
	}

}
