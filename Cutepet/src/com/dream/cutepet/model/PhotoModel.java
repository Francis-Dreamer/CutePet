package com.dream.cutepet.model;

/**
 * 图片的model类
 * 
 * @author 余飞
 *
 */
public class PhotoModel {
	private String time;
	private String[] path;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public String[] getPath() {
		return path;
	}

	public void setPath(String[] path) {
		this.path = path;
	}

}
